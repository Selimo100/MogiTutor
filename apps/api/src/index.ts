import { buildApp } from './app';

const start = async () => {
  const app = await buildApp();
  try {
    const port = 8080;
    const address = await app.listen({ port, host: '0.0.0.0' });
    console.log(`Server listening at ${address}`);
  } catch (err) {
    app.log.error(err);
    process.exit(1);
  }
};

start();
